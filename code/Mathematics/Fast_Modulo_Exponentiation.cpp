/*
Fast Modulo Exponentiation algorithm
Runs on O(log y) time,
calculate x^y mod p
*/

ll modpow(ll x, ll y, ll p)
{
    ll res = 1;
    x = x % p;
    while (y > 0)
    {
        if (y & 1)
            res = (res*x) % p;
        y = y>>1;
        x = (x*x) % p;
    }
    return res;
}
